/**
 * Curated color palette presets for Metro Routes.
 *
 * Each palette defines both light and dark mode colors.
 * The user can switch between palettes in Settings.
 * Icons reference Ionicons names for the theme picker UI.
 */

export interface PaletteColors {
  primary: string;
  primaryMuted: string;
  accent: string;
  success: string;
  warning: string;
  error: string;
  info: string;
}

export interface PaletteDefinition {
  id: string;
  name: string;
  /** Ionicons icon name for the theme picker */
  icon: string;
  /** Fallback emoji (used in minimal contexts) */
  emoji: string;
  light: PaletteColors & {
    background: string;
    surface: string;
    surfaceVariant: string;
    onBackground: string;
    onSurface: string;
    onSurfaceVariant: string;
    onPrimary: string;
  };
  dark: PaletteColors & {
    background: string;
    surface: string;
    surfaceVariant: string;
    onBackground: string;
    onSurface: string;
    onSurfaceVariant: string;
    onPrimary: string;
  };
}

